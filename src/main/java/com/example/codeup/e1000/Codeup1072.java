package com.example.codeup.e1000;

import java.util.Scanner;

public class Codeup1072 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();

        for (int i = 0; i < num; i++) {
            int input = sc.nextInt();
            System.out.println(input);
        }
    }
}
