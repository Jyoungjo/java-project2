package com.example.week3.day2;

public class ForLoopCase2 {
    public static void main(String[] args) {

        for (int i = 0; 2 * i <= 17; i++) {
            System.out.println(i);
        }

        for (int i = 0; i * i <= 50; i++) {
            System.out.println(i);
        } // 제곱근 표현하는 방법
    }
}
